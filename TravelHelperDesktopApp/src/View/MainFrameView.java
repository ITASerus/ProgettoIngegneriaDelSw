    public void setSummaryPanel() {
        SummaryPanelView a = new SummaryPanelView();
        a.setVisible(true);
        a.validate();
        validate();
        changePanel(a);
    }
    
    public void setInfoStructurePanel(Structure structure) {
        if(structure != null) {
            InfoStructurePanelView i = new InfoStructurePanelView(this, structure);
            i.setVisible(true);
            i.validate();
            validate();
            changePanel(i);
        } else {
            setSummaryPanel();
        }    
    }
    
    public void setEditStructurePanel(Structure structure) {
        if(structure != null) {
            EditStructurePanelView e = new EditStructurePanelView(this, structure);
            e.setVisible(true);
            e.validate();
            validate();
            changePanel(e);
        } else {
            setSummaryPanel();
        }    
    }
    
    public void changePanel(JPanel panel) {
        getContentPane().removeAll();
        getContentPane().add(panel); 
        getContentPane().doLayout();
        update(getGraphics());
        revalidate();
    }
    
